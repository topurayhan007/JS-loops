var roastGiven = 0;
while (roastGiven < 7) {
  console.log("Give me a roast, please!!");
  roastGiven++;
}

// for loop
for (var roastGiven = 0; roastGiven < 7; roastGiven++) {
  console.log("Give me a roast, please!!");
}

// simple version of for loop
console.log("Simple For loop");
for (var i = 0; i < 7; i++) {
  console.log(i);
}

// for printing numbers from 1 to 10
for (var i = 0; i <= 10; i++) {
  console.log(i);
}

// even number using for loop
for (var i = 0; i <= 20; i += 2) {
  console.log(i);
}

// odd number using for loop
for (var i = 1; i <= 20; i += 2) {
  console.log(i);
}
